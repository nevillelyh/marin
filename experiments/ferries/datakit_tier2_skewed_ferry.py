"""Datakit tier2 ferry: end-to-end pipeline on a skewed-distribution synthetic dataset.

The dataset is hosted at ``ravwojdyla/datakit-tier2-skewed-v2`` on HuggingFace
(generated by ``scripts/datakit/generate_tier2_skewed.py`` from the FineWeb-Edu
``sample/10BT`` source; ODC-By 1.0). Distribution: 70% log-normal centered at
~5 KB, 30% Pareto alpha=1.1, plus an explicit injection of mega docs in the
[128, 256] MB band — designed to stress the long-doc paths that the FineWeb-Edu
smoke ferry doesn't cover.

The download step uses ``download_hf_step`` so it cache-hits on a region-local
staged copy when one exists at ``$MARIN_PREFIX/raw/<...>``, and falls back to a
fresh HuggingFace download otherwise. We default ``MARIN_PREFIX`` to the
region-local stable marin bucket (e.g. ``gs://marin-us-central1``); pipeline
outputs go to absolute TTL paths under ``marin_temp_bucket(ttl_days=1)`` to
avoid polluting stable storage with per-run CI data.
"""

import json
import logging
import os

from marin.datakit.download.huggingface import download_hf_step
from marin.datakit.normalize import NormalizedData, normalize_step
from marin.execution.artifact import Artifact
from marin.execution.step_runner import StepRunner
from marin.execution.step_spec import StepSpec
from marin.processing.classification.consolidate import (
    FilterConfig,
    FilterType,
    consolidate,
)
from marin.processing.classification.deduplication.fuzzy_dups import (
    FuzzyDupsAttrData,
    compute_fuzzy_dups_attrs,
)
from marin.processing.classification.deduplication.fuzzy_minhash import (
    MinHashAttrData,
    compute_minhash_attrs,
)
from marin.processing.tokenize.tokenize import TokenizeConfig, tokenize
from rigging.filesystem import marin_prefix, marin_temp_bucket, url_to_fs
from rigging.log_setup import configure_logging
from rigging.timing import log_time

logger = logging.getLogger(__name__)

HF_DATASET_ID = "ravwojdyla/datakit-tier2-skewed-v2"
HF_REVISION = "de656ef7cc7c84ceb9892c75a77347d9003c1273"
# Short prefix used in the cache directory so a revision bump produces a fresh
# cache key without invalidating prior versions.
HF_REVISION_SHORT = HF_REVISION[:7]


def build_steps(run_id: str) -> list[StepSpec]:
    ttl_base = marin_temp_bucket(ttl_days=1, prefix=f"datakit-tier2-skewed-smoke/{run_id}")

    download = download_hf_step(
        "datakit-tier2-skewed-smoke/download",
        hf_dataset_id=HF_DATASET_ID,
        revision=HF_REVISION,
        hf_urls_glob=["data/*.parquet"],
        override_output_path=f"raw/datakit-tier2-skewed-v2-{HF_REVISION_SHORT}",
    )

    normalized = normalize_step(
        name="datakit-tier2-skewed-smoke/normalize",
        download=download,
        text_field="content",
        id_field="id",
        relative_input_path="data",
        file_extensions=(".parquet",),
        override_output_path=f"{ttl_base}/normalize",
    )

    minhash = StepSpec(
        name="datakit-tier2-skewed-smoke/minhash",
        deps=[normalized],
        fn=lambda output_path: compute_minhash_attrs(
            source=Artifact.load(normalized, NormalizedData),
            output_path=output_path,
        ),
        override_output_path=f"{ttl_base}/minhash",
    )

    # ~98 source shards / ~46 GiB; modest fan-out for fuzzy_dups CC.
    deduped = StepSpec(
        name="datakit-tier2-skewed-smoke/fuzzy_dups",
        deps=[minhash],
        hash_attrs={"cc_max_iterations": 3},
        fn=lambda output_path: compute_fuzzy_dups_attrs(
            inputs=[Artifact.load(minhash, MinHashAttrData)],
            output_path=output_path,
            max_parallelism=64,
            cc_max_iterations=3,
        ),
        override_output_path=f"{ttl_base}/fuzzy_dups",
    )

    consolidated = StepSpec(
        name="datakit-tier2-skewed-smoke/consolidate",
        deps=[normalized, deduped],
        fn=lambda output_path: consolidate(
            input_path=Artifact.load(normalized, NormalizedData).main_output_dir,
            output_path=output_path,
            filetype="parquet",
            filters=[
                FilterConfig(
                    type=FilterType.KEEP_DOC,
                    attribute_path=Artifact.load(deduped, FuzzyDupsAttrData)
                    .sources[Artifact.load(normalized, NormalizedData).main_output_dir]
                    .attr_dir,
                    name="is_cluster_canonical",
                    attribute_filetype="parquet",
                    keep_if_missing=True,
                ),
            ],
        ),
        override_output_path=f"{ttl_base}/consolidate",
    )

    tokenized = StepSpec(
        name="datakit-tier2-skewed-smoke/tokenize",
        deps=[consolidated],
        hash_attrs={"tokenizer": "gpt2"},
        fn=lambda output_path: tokenize(
            TokenizeConfig(
                train_paths=[consolidated.output_path],
                validation_paths=[],
                cache_path=output_path,
                tokenizer="gpt2",
            )
        ),
        override_output_path=f"{ttl_base}/tokens",
    )

    return [download, normalized, minhash, deduped, consolidated, tokenized]


def _write_status(status: str, prefix: str) -> None:
    """Write ferry run status to FERRY_STATUS_PATH if set."""
    status_path = os.environ.get("FERRY_STATUS_PATH")
    if not status_path:
        return
    payload = json.dumps({"status": status, "marin_prefix": prefix})
    fs, _ = url_to_fs(status_path)
    with fs.open(status_path, "w") as f:
        f.write(payload)
    logger.info("Wrote ferry status to %s", status_path)


def main() -> None:
    configure_logging()
    # Pin MARIN_PREFIX to the region-local stable marin bucket so the download
    # step's relative override_output_path cache-hits on the staged copy when
    # one exists for the iris-picked region. Pipeline outputs are routed to
    # absolute TTL paths in build_steps().
    if not os.environ.get("MARIN_PREFIX"):
        os.environ["MARIN_PREFIX"] = marin_prefix()

    prefix = os.environ["MARIN_PREFIX"]
    logger.info("MARIN_PREFIX=%s", prefix)
    logger.info("HF source: %s @ %s", HF_DATASET_ID, HF_REVISION)
    run_id = os.environ["SMOKE_RUN_ID"]

    _write_status("running", prefix)
    with log_time("Datakit tier2-skewed ferry total wall time"):
        StepRunner().run(build_steps(run_id))
    _write_status("succeeded", prefix)


if __name__ == "__main__":
    main()
